@api.post("/score/{loan_id}")
def score(loan_id: int):
    """Full §11.3 six-part output: (1) calibrated PD, (2) hazard curve — null on
    the TabPFN thin-file path, which is a labeled point estimate, (3) unified
    risk grade, (4) top-5 SHAP drivers, (5) counterfactual, (6) recommended
    action — plus guarded narrative, confidence band and disclosure.

    Features come from the ONLINE feature store (feature_store.get_online_features);
    every call appends an immutable audit_log row."""
    feats, row = _feature_row(loan_id)
    segment = feats["loan_type_segment"]
    model_used = route_segment(feats)

    if model_used == "tabpfn_thin_file":
        hazard_curve = None
        raw_pd = _thin_file_raw(row)
        drivers = tabpfn_top_drivers(
            STATE["thin_file"]["model"], STATE["thin_bg"], row[STATE["thin_file"]["columns"]]
        )
    else:
        curve, raw_pd = _hazard_raw(row)
        hazard_curve = [round(float(h), 5) for h in curve]
        loan_matrix = row.copy()
        loan_matrix["months_since_origination"] = MAX_MONTHS
        X_row = align_columns(build_model_matrix(loan_matrix), STATE["columns"])
        drivers = top_drivers(STATE["explainer"], X_row)

    # Same per-segment isotonic for both engines: the §9.4/§12.8 unified-scale
    # contract (calibrators were fitted on hazard scores; the thin-file PD is
    # mapped through the same ladder so grades stay comparable).
    calibrated = _calibrate(segment, raw_pd)
    grade = pd_to_grade(calibrated)

    narrative = generate_narrative(drivers, grade, calibrated)

    if model_used == "tabpfn_thin_file":
        counterfactual = {
            "feature": None,
            "reason": (
                "counterfactual grid re-scoring is defined for the hazard engine; "
                "the TabPFN thin-file path returns a labeled point estimate only"
            ),
        }
    else:
        counterfactual = find_counterfactual(
            STATE["model"], STATE["columns"], STATE["calibrators"], segment, row, drivers
        )

    band = confidence_band(STATE["bands"], segment, calibrated)

    result = {
        "loan_id": loan_id,
        "segment": segment,
        "sector": feats.get("sector_segment"),
        "data_richness": feats.get("data_richness"),
        "model_used": model_used,
        "point_estimate": model_used == "tabpfn_thin_file",
        "hazard_curve": hazard_curve,
        "raw_cum_pd_12m": round(float(raw_pd), 4),
        "calibrated_pd_12m": round(calibrated, 4),
        "risk_grade": grade,
        "recommended_action": ACTION_BY_GRADE[grade],
        "top_drivers": drivers,
        "narrative": narrative,
        "counterfactual": counterfactual,
        "confidence_band": band,
        "disclosure": DISCLOSURE,
    }

    store.record_audit(
        loan_id,
        endpoint=f"/api/score/{loan_id}",
        model_used=model_used,
        calibrated_pd=calibrated,
        risk_grade=grade,
        narrative_source=narrative["source"],
        payload=result,
    )
    # Cache the explanation so GET /api/explain never re-scores (§17).
    STATE["explanations"][loan_id] = {
        "loan_id": loan_id,
        "risk_grade": grade,
        "calibrated_pd_12m": round(calibrated, 4),
        "shap_values": drivers,
        "narrative": narrative,
        "counterfactual": counterfactual,
        "confidence_band": band,
    }
    return result


