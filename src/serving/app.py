@api.post("/stress-test")
def stress_test(body: StressIn):
    """§12.7 scenario simulator: perturb mapped features on the OOT scored
    slice, re-score through hazard + calibration (pure re-inference), report
    pre/post grade distributions and the delta expected-loss rate.

    Sampling: up to STRESS_MAX_LOANS loans (random_state=42) — the 12-month
    person-period expansion keeps the whole run sub-second; the sample size
    and flag are stated in the response."""
    if body.shock_type not in STRESS_SHOCKS:
        raise HTTPException(422, f"shock_type must be one of {list(STRESS_SHOCKS)}")
    magnitude = body.magnitude
    if abs(magnitude) > 1.0:  # forgiving: -15 means -15%
        magnitude = magnitude / 100.0
    if body.shock_type in ("revenue", "sector_demand") and magnitude <= -1.0:
        raise HTTPException(422, "revenue shock magnitude must be > -1 (a fraction)")

    scored = STATE["scored_join"]
    sampled = len(scored) > STRESS_MAX_LOANS
    sample = scored.sample(STRESS_MAX_LOANS, random_state=42) if sampled else scored
    feats = STATE["features"].loc[sample["SK_ID_CURR"]]

    if body.shock_type == "sector_demand":
        if not body.sector:
            raise HTTPException(422, "sector is required for a sector_demand shock")
        mask = feats["sector_segment"] == body.sector
        if not mask.any():
            known = sorted(STATE["features"]["sector_segment"].dropna().unique())
            raise HTTPException(422, f"unknown sector '{body.sector}'; known: {known}")
    else:
        mask = pd.Series(True, index=feats.index)

    shocked = feats.copy()
    if body.shock_type in ("revenue", "sector_demand"):
        # Revenue/income moves by (1+m); obligation ratios scale by 1/(1+m).
        ratio_factor = 1.0 / (1.0 + magnitude)
        for col in ("credit_income_ratio", "annuity_income_ratio"):
            shocked.loc[mask, col] = shocked.loc[mask, col] * ratio_factor
    else:  # rate shock: EMI burden rises by the given fraction
        shocked.loc[mask, "annuity_income_ratio"] = shocked.loc[
            mask, "annuity_income_ratio"
        ] * (1.0 + magnitude)

    pre_pd, pre_grades = _batch_score(feats)
    post_pd, post_grades = _batch_score(shocked)

    pre_el = float(np.mean(pre_pd) * LGD)
    post_el = float(np.mean(post_pd) * LGD)
    return {
        "shock_type": body.shock_type,
        "magnitude": magnitude,
        "sector": body.sector,
        "n_loans_scored": int(len(feats)),
        "n_loans_shocked": int(mask.sum()),
        "sampled": sampled,
        "sampling_note": (
            f"random sample of {STRESS_MAX_LOANS} of {len(scored)} OOT loans "
            f"(random_state=42); 12-month person-period re-inference"
            if sampled
            else "full OOT scored slice re-scored"
        ),
        "pre_shock_distribution": _grade_distribution(pre_grades),
        "post_shock_distribution": _grade_distribution(post_grades),
        "pre_expected_loss_rate": round(pre_el, 5),
        "post_expected_loss_rate": round(post_el, 5),
        "delta_expected_loss_rate": round(post_el - pre_el, 5),
        "el_convention": EL_CONVENTION,
    }


