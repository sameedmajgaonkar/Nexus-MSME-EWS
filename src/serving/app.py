@api.post("/events/simulate")
def simulate_event(body: EventIn):
    """Publish a simulated GST-filing / bank-transaction / repayment event onto
    the bus (§14). If feature_updates is omitted, a documented default is
    derived from the loan's CURRENT online features:

      gst_filing       (distress) — GST-turnover-drop proxy: a ~30% reported-income
                        drop inflates credit_income_ratio and annuity_income_ratio
                        by 1.4x, and sustained turnover stress surfaces as payment
                        strain on existing obligations (bureau_overdue_flag -> 1,
                        prior_late_rate +0.2). No direct GST features exist in the
                        Home Credit proxy snapshot, so the drop is mapped onto the
                        model-consumed stress features; real GSTN fields plug in
                        here once sandbox data arrives (plan.md §6.1, §12.4).
      bank_transaction (distress) — bureau_overdue_flag -> 1, prior_late_rate +0.2
                        (clipped at 1.0).
      repayment        (positive) — prior_late_rate improves by 20%.

    The consumer thread refreshes the online store, re-scores the loan through
    the full pipeline, and inserts an alert row if the grade moved (otherwise
    a silent risk_timeline row) — poll GET /api/alerts / /api/timeline."""
    if body.type not in EVENT_TYPES:
        raise HTTPException(422, f"type must be one of {list(EVENT_TYPES)}")
    try:
        feats = feature_store.get_online_features(body.loan_id)
    except KeyError:
        raise HTTPException(404, f"Unknown loan_id {body.loan_id}")

    updates = body.feature_updates
    if not updates:
        if body.type == "gst_filing":
            updates = {
                col: round(float(feats[col]) * 1.4, 6)
                for col in ("credit_income_ratio", "annuity_income_ratio")
                if feats.get(col) is not None
            }
            # GST turnover stress shows up as payment strain in this proxy
            # feature space (see docstring) — without these the shock can land
            # entirely on features the tree model never splits on.
            updates["bureau_overdue_flag"] = 1.0
            updates["prior_late_rate"] = round(
                min(1.0, float(feats.get("prior_late_rate") or 0.0) + 0.2), 6
            )
        elif body.type == "bank_transaction":
            base = feats.get("prior_late_rate") or 0.0
            updates = {
                "bureau_overdue_flag": 1.0,
                "prior_late_rate": round(min(1.0, float(base) + 0.2), 6),
            }
        else:  # repayment
            base = feats.get("prior_late_rate") or 0.0
            updates = {"prior_late_rate": round(max(0.0, float(base) * 0.8), 6)}

    event = {"loan_id": body.loan_id, "type": body.type, "feature_updates": updates}
    event_id = STATE["bus"].publish(EVENTS_TOPIC, event)
    return {
        "published": True,
        "event_id": event_id,
        "topic": EVENTS_TOPIC,
        "loan_id": body.loan_id,
        "event_type": body.type,
        "feature_updates": updates,
        "hint": "poll GET /api/alerts (grade moved) or GET /api/timeline/{loan_id} (silent)",
    }


