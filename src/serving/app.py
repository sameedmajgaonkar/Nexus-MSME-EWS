@api.post("/override/{loan_id}")
def override(loan_id: int, body: DecisionIn):
    """§12.5 decision log: accept | modify | override. Reason is MANDATORY for
    both modify and override; a modify records its delta. Append-only."""
    if body.decision not in store.DECISIONS:
        raise HTTPException(422, f"decision must be one of {list(store.DECISIONS)}")
    if body.decision in ("modify", "override") and not (body.reason and body.reason.strip()):
        raise HTTPException(
            422, f"A reason is mandatory when you {body.decision} the AI assessment."
        )

    scored = STATE["scored_test"]
    row = scored[scored["SK_ID_CURR"] == loan_id]
    grade = row.iloc[0]["risk_grade"] if len(row) else None
    pd_val = float(row.iloc[0]["calibrated_pd_12m"]) if len(row) else None

    record_id = store.record_decision(
        loan_id, body.decision, body.reason, grade, pd_val, delta=body.delta
    )
    return {"logged": True, "record_id": record_id}


