@app.on_event("startup")
def load_artifacts():
    # joblib.load here only touches models/*.joblib artifacts produced by this
    # repo's own phase scripts (trusted, project-internal) — same pattern as
    # scripts/run_phase4/7; never pointed at user-supplied files.
    bundle = joblib.load(ROOT / "models" / "hazard.joblib")
    STATE["model"] = bundle["model"]
    STATE["columns"] = bundle["columns"]
    STATE["calibrators"] = joblib.load(ROOT / "models" / "calibrators.joblib")
    STATE["explainer"] = build_explainer(bundle["model"])
    STATE["bands"] = joblib.load(ROOT / "models" / "confidence_bands.joblib")

    thin = joblib.load(ROOT / "models" / "thin_file_tabpfn.joblib")
    STATE["thin_file"] = thin

    STATE["features"] = pd.read_parquet(
        ROOT / "data" / "processed" / "serving_features_enriched.parquet"
    ).set_index("SK_ID_CURR", drop=False)
    scored = pd.read_parquet(ROOT / "data" / "processed" / "phase4_scored_test.parquet")
    STATE["scored_test"] = scored
    STATE["scored_join"] = scored.merge(
        STATE["features"][["SK_ID_CURR", "sector_segment", "data_richness"]].reset_index(
            drop=True
        ),
        on="SK_ID_CURR",
        how="left",
    )
    # SHAP background pool for the thin-file permutation explainer.
    ntc = STATE["features"][STATE["features"]["data_richness"] == "ntc_ntb"]
    STATE["thin_bg"] = ntc[thin["columns"]].sample(
        min(THIN_FILE_BACKGROUND_ROWS, len(ntc)), random_state=42
    )

    STATE["metrics"] = {
        name: json.loads((ROOT / "models" / f"{name}.json").read_text())
        for name in [
            "phase2_metrics",
            "phase3_metrics_target",
            "phase3_metrics_delinquency",
            "phase4_metrics",
            "phase5_metrics",
            "phase6_metrics",
        ]
        if (ROOT / "models" / f"{name}.json").exists()
    }
    STATE["explanations"] = {}
    STATE["fairness_cache"] = None

    # Governance store: tables + append-only triggers.
    store.init_db()

    # Feature store: materialize the scoreable book into the ONLINE store once
    # (empty-store check keeps restarts instant); unseen loans write-through.
    if feature_store.online_count() == 0:
        feature_store.materialize(loan_ids=[int(i) for i in scored["SK_ID_CURR"]])

    # Streaming: event bus + feature-refresh consumer thread (plan.md §14).
    bus = get_bus()
    bus.subscribe(EVENTS_TOPIC, make_feature_refresh_handler(_pd_and_grade))
    bus.start()
    STATE["bus"] = bus


@app.on_event("shutdown")
def stop_bus():
    bus = STATE.get("bus")
    if bus is not None:
        bus.stop()


# ---------------------------------------------------------------- helpers

def _feature_row(loan_id: int) -> tuple[dict, pd.DataFrame]:
    """Serving-path feature access: ONLINE store (write-through on first miss)."""
    try:
        feats = feature_store.get_online_features(loan_id)
    except KeyError:
        raise HTTPException(404, f"Unknown loan_id {loan_id}")
    row = pd.DataFrame([feats])
    for col in row.columns:
        if col not in STRING_COLS:
            row[col] = pd.to_numeric(row[col], errors="coerce")
    return feats, row


def _calibrate(segment: str, raw_pd: float) -> float:
    iso = STATE["calibrators"].get(segment, STATE["calibrators"][GLOBAL_KEY])
    return float(iso.predict([raw_pd])[0])


def _hazard_raw(row: pd.DataFrame) -> tuple[np.ndarray, float]:
    months = pd.concat([row] * MAX_MONTHS, ignore_index=True)
    months["months_since_origination"] = np.arange(1, MAX_MONTHS + 1)
    X = align_columns(build_model_matrix(months), STATE["columns"])
    curve = STATE["model"].predict_proba(X)[:, 1]
    return curve, cumulative_pd(curve)


def _thin_file_raw(row: pd.DataFrame) -> float:
    """TabPFN single-point PD (plan.md §9.3) — clearly a point estimate, no curve."""
    X = row[STATE["thin_file"]["columns"]]
    return float(STATE["thin_file"]["model"].predict_proba(X)[:, 1][0])


def _pd_and_grade(loan_id: int) -> dict:
    """Lightweight full-pipeline re-score (router -> raw -> calibrate -> grade).

    Used by the streaming feature-refresh consumer; no SHAP/narrative work."""
    feats, row = _feature_row(loan_id)
    model_used = route_segment(feats)
    if model_used == "tabpfn_thin_file":
        raw = _thin_file_raw(row)
    else:
        _, raw = _hazard_raw(row)
    calibrated = _calibrate(feats["loan_type_segment"], raw)
    return {
        "calibrated_pd_12m": calibrated,
        "risk_grade": pd_to_grade(calibrated),
        "model_used": model_used,
    }


def _grade_distribution(grades: pd.Series | list) -> dict:
    counts = pd.Series(grades).value_counts()
    return {g: int(counts.get(g, 0)) for g in GRADES}


def _batch_score(feats: pd.DataFrame) -> tuple[np.ndarray, list[str]]:
    """Vectorized hazard + calibration re-scoring of a loan slice (§12.7).

    12-month person-period expansion of every row in one LightGBM call —
    pure re-inference, no retraining."""
    n = len(feats)
    rep = feats.loc[feats.index.repeat(MAX_MONTHS)].reset_index(drop=True)
    rep["months_since_origination"] = np.tile(np.arange(1, MAX_MONTHS + 1), n)
    X = align_columns(build_model_matrix(rep), STATE["columns"])
    hazards = STATE["model"].predict_proba(X)[:, 1].reshape(n, MAX_MONTHS)
    raw = 1.0 - np.prod(1.0 - hazards, axis=1)
    segments = feats["loan_type_segment"].reset_index(drop=True)
    calibrated = apply_calibration(STATE["calibrators"], segments, pd.Series(raw))
    return calibrated, [pd_to_grade(p) for p in calibrated]


