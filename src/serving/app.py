class DecisionIn(BaseModel):
    decision: str  # 'accept' | 'modify' | 'override'
    reason: str | None = None
    delta: dict | str | None = None  # what a 'modify' changed


class StressIn(BaseModel):
    """§12.7 scenario: magnitude is a signed FRACTION (|m| > 1 is read as a
    percentage). revenue/sector_demand: fractional revenue change (-0.15 =
    15% drop -> obligation ratios scale by 1/(1+m)). rate: fractional increase
    in the annuity/EMI burden (e.g. 0.10 ~ +200bps on a mid-tenor loan)."""

    shock_type: str  # 'revenue' | 'rate' | 'sector_demand'
    magnitude: float
    sector: str | None = None


class EventIn(BaseModel):
    loan_id: int
    type: str  # 'gst_filing' | 'bank_transaction' | 'repayment'
    feature_updates: dict[str, float] | None = None


